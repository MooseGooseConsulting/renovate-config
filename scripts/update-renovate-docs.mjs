import { mkdir, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const outputPath = resolve(root, '.agents/skills/renovate-config/references/renovate-docs-snapshot.md');

const pages = [
  {
    title: 'Configuration Options',
    url: 'https://docs.renovatebot.com/configuration-options/',
    sections: [
      'dependencydashboard',
      'dependencydashboardapproval',
      'prconcurrentlimit',
      'branchconcurrentlimit',
      'prcreation',
      'prhourlylimit',
      'schedule',
      'updatenotscheduled',
      'rebasewhen',
      'minimumreleaseage',
      'packagerules',
      'groupname',
      'reviewers',
      'reviewersfromcodeowners',
      'assignees',
      'vulnerabilityalerts'
    ]
  },
  {
    title: 'Shareable Config Presets',
    url: 'https://docs.renovatebot.com/config-presets/',
    sections: [
      'extending-from-a-preset',
      'preset-hosting',
      'preset-file-naming',
      'github-hosted-presets'
    ]
  },
  {
    title: 'Mend-hosted Apps Configuration',
    url: 'https://docs.renovatebot.com/mend-hosted/hosted-apps-config/',
    sections: [
      'inherited-config',
      'validating-configuration'
    ]
  }
];

const fetchedAt = new Date().toISOString();
const chunks = [
  '# Renovate Docs Snapshot',
  '',
  `Generated: ${fetchedAt}`,
  '',
  'This file is generated by `npm run docs:update`. Do not hand-edit it.',
  'Use the linked official docs as authority if this extraction looks incomplete.',
  ''
];

for (const page of pages) {
  const html = await fetchText(page.url);
  chunks.push(`## ${page.title}`, '', `Source: ${page.url}`, '');

  for (const sectionId of page.sections) {
    const sectionHtml = extractSection(html, sectionId);
    if (!sectionHtml) {
      chunks.push(`### ${sectionId}`, '', 'Extraction failed; inspect the source URL directly.', '');
      continue;
    }

    const text = htmlToMarkdownish(sectionHtml).trim();
    chunks.push(`### ${sectionId}`, '', truncate(text, 1800), '');
  }
}

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, `${chunks.join('\n')}\n`, 'utf8');
console.log(`Wrote ${outputPath}`);

async function fetchText(url) {
  const response = await fetch(url, {
    headers: {
      'user-agent': 'MooseGoose renovate-config docs updater'
    }
  });

  if (!response.ok) {
    throw new Error(`Failed to fetch ${url}: ${response.status} ${response.statusText}`);
  }

  return response.text();
}

function extractSection(html, id) {
  const heading = new RegExp(`<h([23]) id="${escapeRegExp(id)}"[^>]*>[\\s\\S]*?<\\/h\\1>`, 'i');
  const match = heading.exec(html);
  if (!match) return null;

  const start = match.index;
  const level = match[1];
  const nextHeading = new RegExp(`<h${level} id="[^"]+"`, 'gi');
  nextHeading.lastIndex = start + match[0].length;
  const next = nextHeading.exec(html);
  return html.slice(start, next?.index ?? html.length);
}

function htmlToMarkdownish(html) {
  return html
    .replace(/<script[\s\S]*?<\/script>/gi, '')
    .replace(/<style[\s\S]*?<\/style>/gi, '')
    .replace(/<pre[^>]*><code[^>]*>([\s\S]*?)<\/code><\/pre>/gi, (_, code) => `\n\n\`\`\`json\n${decodeEntities(stripTags(code)).trim()}\n\`\`\`\n\n`)
    .replace(/<code[^>]*>([\s\S]*?)<\/code>/gi, (_, code) => `\`${decodeEntities(stripTags(code)).trim()}\``)
    .replace(/<h[23][^>]*>([\s\S]*?)<\/h[23]>/gi, (_, title) => `\n#### ${decodeEntities(stripTags(title)).replace('¶', '').trim()}\n`)
    .replace(/<li[^>]*>/gi, '\n- ')
    .replace(/<\/p>/gi, '\n')
    .replace(/<br\s*\/?>/gi, '\n')
    .replace(/<[^>]+>/g, '')
    .split('\n')
    .map((line) => decodeEntities(line).trim())
    .filter(Boolean)
    .join('\n')
    .replace(/\n{3,}/g, '\n\n');
}

function stripTags(value) {
  return value.replace(/<[^>]+>/g, '');
}

function decodeEntities(value) {
  return value
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'")
    .replace(/&lt;/g, '<')
    .replace(/&gt;/g, '>')
    .replace(/&amp;/g, '&')
    .replace(/&nbsp;/g, ' ');
}

function truncate(value, maxLength) {
  if (value.length <= maxLength) return value;
  return `${value.slice(0, maxLength).trimEnd()}\n\n[truncated; inspect the source URL for the full section]`;
}

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}
